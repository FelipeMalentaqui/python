# Faça um programa que peça 4 notas bimestre e mostra a media

nota1 = input('informe o valor 1: ')
nota2 = input('informe o valor 2: ')
nota3 = input('informe o valor 3: ')
nota4 = input('informe o valor 4: ')

totalDasNotas = float(nota1) + float(nota2) + float(nota3) + float(nota4)

mediaDasNotas = totalDasNotas / 4

print('a soma das notas é: {}' . format(totalDasNotas))


print('a media das notas é: {}' . format(mediaDasNotas))





