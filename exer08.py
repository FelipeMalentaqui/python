
#Faça um programa que pergunte quanto você ganha por hora e o numero de horas trabalhada no mes. Calcule e moste o total do seu salario do mes referido


salario = input('Quanto você ganha por hora?:')

horas = input('Quantas horas você trabalha no mês?:')

salarioMensal = int(salario) * int(horas)

print('O meu salario mensal é {:.2f}' . format(salarioMensal))



